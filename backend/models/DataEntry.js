import mongoose from "mongoose";

const dataEntrySchema = new mongoose.Schema({
  end_year: String,
  intensity: Number,
  sector: String,
  topic: String,
  insight: String,
  url: String,
  region: String,
  start_year: String,
  impact: String,
  added: String,
  published: String,
  country: String,
  relevance: Number,
  pestle: String,
  source: String,
  title: String,
  likelihood: Number,
  city: String,
  swot: String,
});

const DataEntry = mongoose.model("DataEntry", dataEntrySchema);

export default DataEntry;